              <h5 class="card-title">About Malaria</h5>
              <p>Malaria is a life-threatening disease caused by Plasmodium parasites, which are transmitted to humans through the bites of infected female Anopheles mosquitoes. There are several types of malaria parasites, with Plasmodium falciparum being the most severe and deadly. Malaria is prevalent in tropical and subtropical regions, particularly in Africa, Asia, and Latin America. Symptoms typically appear 10-15 days after being bitten and include fever, chills, headache, muscle pain, nausea, and vomiting. Severe malaria can lead to anemia, respiratory distress, organ failure, or even death if not treated promptly.</p>

              <h5 class="card-title">How to treate</h5>
              <p>The treatment of malaria depends on the type of parasite and the severity of the disease:
                  <ol>
                    <li>Uncomplicated Malaria: This is treated using antimalarial drugs, often in combination to prevent resistance. The most common medications are:</li>
                    <ul>
                      <li>Artemisinin-based Combination Therapies (ACTs): These are the preferred treatment for P. falciparum malaria. Common ACTs include artemether-lumefantrine and artesunate-amodiaquine.</li>
                      <li>Chloroquine: This is effective against P. vivax and P. ovale malaria, but many strains of P. falciparum have developed resistance to it.</li>
                    </ul>
                    <li>Severe Malaria: In cases where malaria is life-threatening, treatment must be initiated as soon as possible with intravenous (IV) drugs such as:</li>
                    <ul>
                      <li>Intravenous Artesunate: This is used for severe malaria caused by P. falciparum and is administered for 24-48 hours until the patient can take oral medications.</li>
                      <li>Quinine or Quinidine: Though less common today, these can be used in cases where artesunate is unavailable.</li>
                    </ul>
                    Preventive Treatment: For travelers or residents in high-risk areas, chemoprophylaxis (preventive medication) is recommended. Common preventive drugs include atovaquone-proguanil, doxycycline, and mefloquine.

                    Management of Symptoms: Supportive treatments like hydration, anti-fever medications, and blood transfusions (for severe anemia) may be necessary.
                  </ol>
                    Preventing Malaria
                    Preventive measures include:

                    Using insecticide-treated bed nets (ITNs).
                    Indoor Residual Spraying (IRS) in homes to kill mosquitoes.
                    Personal protective measures like wearing long-sleeved clothing and using mosquito repellents.
                    With prompt and appropriate treatment, malaria can be effectively managed, preventing severe complications and saving lives.</p>
